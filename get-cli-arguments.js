const argv = require('yargs')
  .usage('Usage: $0 -g [path/to/gif/folder] -t [path/to/temp/folder] -p [portnumber]')
  .default('g', 'gifs')
  .alias('g', 'gifpath')
  .describe('g', 'Folder to save all GIFs into')
  .default('n', false)
  .alias('n', 'notheadless')
  .default('t', 'temp')
  .alias('t', 'tempfolder')
  .describe('t', 'Folder to save temp files to')
  .default('p', 5000)
  .alias('p', 'port')
  .describe('p', 'Port to host server on (or port server is hosted on)')
	.default('f', false)
	.alias('f', 'forceffmpeg')
	.describe('f', 'Whether to enforce ffmpeg usage for all animations')
	.default('a', '')
	.alias('a', 'absolutepathtoffmpeg')
	.describe('a', 'Absolute path to the ffmpeg executable')
  .alias('s', 'sheetonly')
  .describe('s', 'Whether to output sprite sheets instead of GIFs')
  .default('s', false)
  .help('h')
  .alias('h', 'help')
  .argv;

module.exports = argv;